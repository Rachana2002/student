    private void jButton1_removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1_removeActionPerformed
      
//        when we delete a student record we must delete all scores affected to the student
//        we have to add a constraint to score table to do this 
//        constraint -> foreign key + on delete cascade

            /*           
            ALTER TABLE score 
            ADD CONSTRAINT fk_score_student 
            FOREIGN KEY (student_id) 
            REFERENCES student (Id) 
            ON DELETE CASCADE;

            */
        if(jTextField1_id.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "NO Student Selected");
        }
        else
        {
            int id = Integer.valueOf(jTextField1_id.getText());
            std.insertUpdateDeleteStudent('d', id, null, null, null, null, null, null);
            std.fillStudent(jTable2, "");
            jTable2.setModel(new DefaultTableModel(null,new Object[]{"ID","FirstName","Lastname","Gender","BirthDate","Phone","Address"}));
            std.fillStudent(jTable2, jTextField1_value.getText());
            MainForm.jLabel_studentCount.setText(" Student Count = "+Integer.toString(MyFunction.countData("student")));
            
            jTextField1_id.setText("");
            jTextArea1_address.setText("");
            jTextField1_firstname1.setText("");
            jTextField1_lastname.setText("");
            jTextField1_phone.setText("");
            jRadioButton2_male.setSelected(false);
            jRadioButton1_female.setSelected(false);
            jDateChooser_birthdate.setDate(null);
            

        }      
    }//GEN-LAST:event_jButton1_removeActionPerformed

