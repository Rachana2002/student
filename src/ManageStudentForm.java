    public boolean verifText()
    {
        if(jTextField1_firstname1.getText().equals("") || jTextField1_lastname.getText().equals("") 
           || jTextField1_phone.getText().equals("") || jTextArea1_address.getText().equals("") 
           || jTextField1_id.getText().equals("") || jDateChooser_birthdate.getDate() == null )
        {
            JOptionPane.showMessageDialog(null, "One or More Empty Fields");
            return false;
        }
//        choose a date higher then current date 
        else if(jDateChooser_birthdate.getDate().compareTo(new Date()) > 0)
        {
            JOptionPane.showMessageDialog(null, "Invalid Date!");
            return false;
        }
        else
        {
            return true;
        }
    }
    private void jButton1_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1_editActionPerformed
        String fname = jTextField1_firstname1.getText();
        String lname = jTextField1_lastname.getText();
        String phone = jTextField1_phone.getText();
        String address = jTextArea1_address.getText();
        int id = Integer.valueOf(jTextField1_id.getText());
        String gender = "";
        if(jRadioButton2_male.isSelected())
        {
            gender = "Male";
        }
        else if(jRadioButton1_female.isSelected())
        {
            gender = "Female";
        }
        
        if(verifText())
        {
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            String bdate = dateFormat.format(jDateChooser_birthdate.getDate());
            Student std = new Student();
            std.insertUpdateDeleteStudent('u', id, fname, lname, gender, bdate, phone, address);
//            MainForm.jLabel_studentCount.setText(" Student Count = "+Integer.toString(MyFunction.countData("student")));
         
//            refresh jTable
                ManageStudentForm.jTable2.setModel(new DefaultTableModel(null,new Object[]{"ID","FirstName","Lastname","Gender","BirthDate","Phone","Address"}));
                std.fillStudent(ManageStudentForm.jTable2, "");
        }
        
    }//GEN-LAST:event_jButton1_editActionPerformed
