    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        rowIndex = jTable2.getSelectedRow();
        
        
        if(model.getValueAt(rowIndex, 3).toString().equals("Female"))
        {
            jRadioButton1_female.setSelected(true);
            jRadioButton2_male.setSelected(false);
        }
        else
        {
            jRadioButton2_male.setSelected(true);
            jRadioButton1_female.setSelected(false);
        }
        jTextField1_id.setText(model.getValueAt(rowIndex, 0).toString());
        jTextField1_firstname1.setText(model.getValueAt(rowIndex, 1).toString());
        jTextField1_lastname.setText(model.getValueAt(rowIndex, 2).toString());
        jTextField1_phone.setText(model.getValueAt(rowIndex, 5).toString());
        jTextArea1_address.setText(model.getValueAt(rowIndex, 6).toString());
        
        Date bdate;
        try {
            bdate = new SimpleDateFormat("yyyy-MM-dd").parse(model.getValueAt(rowIndex, 4).toString());
            jDateChooser_birthdate.setDate(bdate);
        
        } catch (ParseException ex) {
            Logger.getLogger(ManageStudentForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jTable2MouseClicked

